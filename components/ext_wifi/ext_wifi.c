static const char *TAG = "ext_wifi";

static void wifi_event_handler(void *arg,
                               esp_event_base_t event_base,
                               int32_t event_id,
                               void *event_data)
{
    if (event_base == IP_EVENT && event_id == IP_EVENT_STA_GOT_IP)
    {
        ESP_LOGI(TAG, "WiFi GOT IP -> start MQTT");

        initial();                       // init mqtt struct
        Application.mqtt.mqttAppStart(); // start mqtt
    }
}

void ext_wifi_init(void)
{
    esp_netif_init();
    esp_event_loop_create_default();
    esp_netif_create_default_wifi_sta();

    wifi_init_config_t cfg = WIFI_INIT_CONFIG_DEFAULT();

    esp_wifi_init(&cfg);

    esp_event_handler_instance_register(IP_EVENT,
                                    IP_EVENT_STA_GOT_IP,
                                    &wifi_event_handler,
                                    NULL,
                                    NULL);
}

esp_err_t ext_wifi_connect(const char *ssid, const char *pass)
{
    esp_err_t ret = -1;
    wifi_config_t wifi_config = {0};

    strcpy((char*)wifi_config.sta.ssid, ssid);
    strcpy((char*)wifi_config.sta.password, pass);

    esp_wifi_set_mode(WIFI_MODE_STA);

    esp_wifi_set_config(WIFI_IF_STA, &wifi_config);

    esp_wifi_start();

    ret = esp_wifi_connect();

    ESP_LOGI(TAG,"wifi connect start | ret %u", ret);
    return ret;

}
